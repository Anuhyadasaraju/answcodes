def commom_elements(l1, l2):
    common_ele = []
    for i in list1:
        if i in list2:
            common_ele.append(i)
    return common_ele

list1 = [1,2,3,4]
list2 = [4,5,6,2,3,8]
common = commom_elements(list1, list2)
print(common)